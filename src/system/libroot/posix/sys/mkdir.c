#include <sys/stat.h>
#include <syscalls.h>
#include <errno.h>


#define RETURN_AND_SET_ERRNO(err) \
	if (err < 0) { \
		errno = err; \
		return -1; \
	} \
	return err;


int
mkdir(const char* path, mode_t mode)
{
	status_t status = _kern_create_dir(-1, path, mode);

	RETURN_AND_SET_ERRNO(status);
}
